"use client";

import Image from "next/image";
import { useEffect, useRef, type CSSProperties } from "react";
import styles from "./home-canvas-demos.module.css";

// Each skin is a different look the user asks Stella to become. The request
// reads as a short user message (3-4 words) that appends as the image arrives.
const SKINS = [
  {
    src: "/app-skins/cozy.jpg",
    alt: "Soft pastel cozy home screen generated by Stella.",
    request: "Make it cozy",
  },
  {
    src: "/app-skins/terminal.jpg",
    alt: "Green terminal home screen generated by Stella.",
    request: "Give me a terminal",
  },
  {
    src: "/app-skins/anime.jpg",
    alt: "Anime sunset home screen generated by Stella.",
    request: "Anime sunset vibes",
  },
  {
    src: "/app-skins/sports.jpg",
    alt: "Sports stadium home screen generated by Stella.",
    request: "Sports mode, please",
  },
  {
    src: "/app-skins/minimal.jpg",
    alt: "Clean minimal home screen generated by Stella.",
    request: "Keep it minimal",
  },
];

export function HomeCanvasDemos() {
  const flowRef = useRef<HTMLDivElement>(null);
  const firstShotRef = useRef<HTMLElement | null>(null);
  const stepRefs = useRef<(HTMLDivElement | null)[]>([]);

  useEffect(() => {
    const flow = flowRef.current;
    if (!flow) return;
    const reduce = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
    const narrow = window.matchMedia("(max-width: 760px)");

    // Overlap = half the rendered image height so each new skin covers the
    // bottom half of the previous one. Skipped on narrow screens.
    const measure = () => {
      const shot = firstShotRef.current;
      if (narrow.matches || !shot || !shot.offsetHeight) {
        flow.style.removeProperty("--overlap");
        return;
      }
      flow.style.setProperty("--overlap", `${shot.offsetHeight * 0.5}px`);
    };

    let ro: ResizeObserver | undefined;
    if (typeof ResizeObserver !== "undefined" && firstShotRef.current) {
      ro = new ResizeObserver(measure);
      ro.observe(firstShotRef.current);
    }
    measure();

    if (reduce) {
      stepRefs.current.forEach((el) => el?.setAttribute("data-revealed", "true"));
      return () => ro?.disconnect();
    }

    // Each step snaps in (and its bubble appends) the moment it scrolls into
    // view; once revealed it stays, so the thread accumulates.
    const io = new IntersectionObserver(
      (entries) => {
        for (const e of entries) {
          if (e.isIntersecting) {
            e.target.setAttribute("data-revealed", "true");
            io.unobserve(e.target);
          }
        }
      },
      { rootMargin: "0px 0px -28% 0px", threshold: 0.01 },
    );
    stepRefs.current.forEach((el) => el && io.observe(el));

    return () => {
      ro?.disconnect();
      io.disconnect();
    };
  }, []);

  return (
    <section
      className="grid-shell section-border home-atlas-section"
      data-reveal
      aria-label="Make Stella yours"
    >
      <div
        className="home-atlas-heading"
        data-reveal-child
        style={{ ["--reveal-index" as string]: 0 }}
      >
        <h2>Make Stella yours.</h2>
      </div>

      <div className={styles.flow} ref={flowRef}>
        {SKINS.map((skin, i) => (
          <div
            className={styles.step}
            key={skin.src}
            ref={(el) => {
              stepRefs.current[i] = el;
            }}
            style={{ zIndex: i + 1 } as CSSProperties}
          >
            <figure
              className={styles.shotWrap}
              ref={i === 0 ? firstShotRef : undefined}
            >
              <Image
                src={skin.src}
                alt={skin.alt}
                width={1600}
                height={1000}
                quality={82}
                sizes="(max-width: 760px) 92vw, 50rem"
                loading={i === 0 ? "eager" : "lazy"}
                className={styles.shot}
              />
            </figure>

            <div className={styles.bubbleRow}>
              <span className={styles.bubble}>{skin.request}</span>
            </div>
          </div>
        ))}
      </div>
    </section>
  );
}
